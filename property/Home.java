    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String type = TypeInput.getSelectedItem().toString();
        String name = NameInput.getText();
        String phone = PhoneInput.getText();
        String intersted = interInput.getText();
        String Last =lastInput.getText();
        Dictionary info = new Hashtable();
        if(TypeInput.getSelectedItem().equals("Compound"))
        {
            info.put("Pool",PoolInput.getText());
            info.put("Security_guard",SecurityInput.getText());
            info.put("Gym",GymInput.getText());
        }
        else if(TypeInput.getSelectedItem().equals("Villa"))
        {
            info.put("Watchman",WatchmenInput.getText());
            info.put("Ac_Type",ACInput.getText());
            info.put("Cameras",CamsInput.getText());
        }
        this.Propertiy.addAddLead(name, type, phone, Last, intersted, info);
        updateList();
        this.AddLead.setVisible(false);
        this.Table.setVisible(true);
        NameInput.setText("");
        PoolInput.setText("");
        SecurityInput.setText("");
        GymInput.setText("");
        WatchmenInput.setText("");
        ACInput.setText("");
        CamsInput.setText("");
        PhoneInput.setText("");
        interInput.setText("");
        lastInput.setText("");
    
    }//GEN-LAST:event_jButton1ActionPerformed


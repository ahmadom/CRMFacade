int i = this.LeadList.getSelectedIndex();
        AddLead it = this.Propertiy.getAddLead(i);
        if(it.getType().equals("Villa"))
       {
           this.LeadDetails.setText("Name: " +it.getName() 
                    + "\nType: "+it.getType()+"\nPhone:"+it.getPhone()+"\nInterested:"+it.getInter()+"\nLast:"+it.getLast()
            +"\nAc_Type:"+((Villa)it).getAC_Type()+"\nCameras:"+((Villa)it).getCameras()+ "\nWatchman: " + ((Villa)it).getWatchman());
       }
        else{
             this.LeadDetails.setText("Name: " +it.getName() + "\nType: "+it.getType()+"\nPhone:"+it.getPhone()+"\nInterested:"+it.getInter()+"\nLast:"+it.getLast()
            +"\nSecurity_guard:"+((Compound)it).getSecurity_guard()+"\npool:"+((Compound)it).getPool()+"\nGym:"+((Compound)it).getGym());
        }  
